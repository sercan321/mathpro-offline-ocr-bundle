class GaussRealMathLiveReviewDraftEnvelope {
  const GaussRealMathLiveReviewDraftEnvelope._({
    required this.requestId,
    required this.captureId,
    required this.engineLabel,
    required this.sourcePhase,
    required this.editableLatexDraft,
    required this.primaryLatexCandidate,
    required this.alternativeLatexCandidates,
    required this.selectedCandidateIndex,
    required this.confidence,
    required this.warnings,
    required this.engineMetadata,
    required this.imageInputMetadata,
    required this.mathLiveReviewRequired,
    required this.reviewDraftEditable,
    required this.userApprovalRequiredBeforeWorkspaceImport,
    required this.workspaceImportPrepared,
    required this.directWorkspaceMutationAttempted,
    required this.directSolveGraphSolutionHistoryAttempted,
    required this.errorCode,
  });

  factory GaussRealMathLiveReviewDraftEnvelope.fromQ283Candidate({
    required String captureId,
    required GaussImageToLatexCandidateEnvelope candidate,
    required Map<String, String> imageInputMetadata,
  }) {
    final primary = candidate.primaryLatexCandidate.trim();
    final hasCandidate = primary.isNotEmpty && candidate.confidence > 0 && candidate.errorCode.trim().isEmpty;
    return GaussRealMathLiveReviewDraftEnvelope._(
      requestId: candidate.requestId,
      captureId: captureId,
      engineLabel: candidate.engineLabel,
      sourcePhase: GaussOcrResultRealMathLiveReviewRuntimeBindingPolicy.sourcePhase,
      editableLatexDraft: hasCandidate ? primary : r'\text{MathLive review pending}',
      primaryLatexCandidate: candidate.primaryLatexCandidate,
      alternativeLatexCandidates: candidate.alternativeLatexCandidates.take(GaussOcrResultRealMathLiveReviewRuntimeBindingPolicy.maxAlternativeCandidateCount).toList(growable: false),
      selectedCandidateIndex: 0,
      confidence: candidate.confidence,
      warnings: candidate.warnings,
      engineMetadata: candidate.engineMetadata,
      imageInputMetadata: imageInputMetadata,
      mathLiveReviewRequired: true,
      reviewDraftEditable: true,
      userApprovalRequiredBeforeWorkspaceImport: true,
      workspaceImportPrepared: false,
      directWorkspaceMutationAttempted: false,
      directSolveGraphSolutionHistoryAttempted: false,
      errorCode: hasCandidate ? '' : 'reviewRuntimeBindingFeatureFlagOff',
    );
  }

  factory GaussRealMathLiveReviewDraftEnvelope.blockedFeatureFlagOff({
    required String requestId,
    required String captureId,
  }) {
    return GaussRealMathLiveReviewDraftEnvelope._(
      requestId: requestId,
      captureId: captureId,
      engineLabel: GaussOcrResultRealMathLiveReviewRuntimeBindingPolicy.selectedEngineLabel,
      sourcePhase: GaussOcrResultRealMathLiveReviewRuntimeBindingPolicy.phase,
      editableLatexDraft: r'\text{MathLive review binding pending}',
      primaryLatexCandidate: r'\text{Q284 review binding is default-off}',
      alternativeLatexCandidates: const <String>[],
      selectedCandidateIndex: 0,
      confidence: 0,
      warnings: const <String>['Q284 defines the real MathLive review runtime binding envelope only; real OCR inference and workspace import remain blocked.'],
      engineMetadata: const <String, String>{'phase': GaussOcrResultRealMathLiveReviewRuntimeBindingPolicy.phase},
      imageInputMetadata: const <String, String>{},
      mathLiveReviewRequired: true,
      reviewDraftEditable: true,
      userApprovalRequiredBeforeWorkspaceImport: true,
      workspaceImportPrepared: false,
      directWorkspaceMutationAttempted: false,
      directSolveGraphSolutionHistoryAttempted: false,
      errorCode: 'reviewRuntimeBindingFeatureFlagOff',
    );
  }

  final String requestId;
  final String captureId;
  final String engineLabel;
  final String sourcePhase;
  final String editableLatexDraft;
  final String primaryLatexCandidate;
  final List<String> alternativeLatexCandidates;
  final int selectedCandidateIndex;
  final double confidence;
  final List<String> warnings;
  final Map<String, String> engineMetadata;
  final Map<String, String> imageInputMetadata;
  final bool mathLiveReviewRequired;
  final bool reviewDraftEditable;
  final bool userApprovalRequiredBeforeWorkspaceImport;
  final bool workspaceImportPrepared;
  final bool directWorkspaceMutationAttempted;
  final bool directSolveGraphSolutionHistoryAttempted;
  final String errorCode;

  bool get hasEditableCandidate => editableLatexDraft.trim().isNotEmpty && editableLatexDraft.length <= GaussOcrResultRealMathLiveReviewRuntimeBindingPolicy.maxEditableLatexLength;
  bool get canOpenEditableMathLiveReview => mathLiveReviewRequired && reviewDraftEditable && hasEditableCandidate && !directWorkspaceMutationAttempted && !directSolveGraphSolutionHistoryAttempted;
  bool get canImportToWorkspace => false;
  bool get canEvaluate => false;
  bool get canGraph => false;
  bool get canWriteSolutionOrHistory => false;
}

class GaussOcrResultRealMathLiveReviewRuntimeBinding {
  const GaussOcrResultRealMathLiveReviewRuntimeBinding._();

  static GaussOcrResultRealMathLiveReviewRuntimeBindingResult evaluate(GaussOcrResultRealMathLiveReviewRuntimeBindingInput input) {
    GaussOcrResultRealMathLiveReviewRuntimeBindingResult reject(GaussOcrResultRealMathLiveReviewRuntimeBindingDecision decision, String reason) {
      return GaussOcrResultRealMathLiveReviewRuntimeBindingResult._(
        decision: decision,
        reviewRuntimeBindingStaticReady: false,
        readyForQ285ApprovedWorkspaceImportRuntimeCourtOnly: false,
        rejectionReasons: <String>[reason],
        canOpenEditableMathLiveReviewInQ284: false,
        canImportWorkspaceWithoutUserApproval: false,
        canEvaluate: false,
        canGraph: false,
        canWriteSolutionOrHistory: false,
        canClaimReviewBindingPassInQ284: false,
        canClaimOcrPassInQ284: false,
        bindingLabel: input.bindingLabel,
      );
    }

    if (!input.q283FirstRealImageToLatexInferenceCourtPresent || !input.acceptedQ283ImageToLatexCandidateEvidence) {
      return reject(GaussOcrResultRealMathLiveReviewRuntimeBindingDecision.blockedMissingQ283ImageToLatexInferenceCourt, 'missingQ283ImageToLatexInferenceCourtOrCandidateEvidence');
    }
    if (!input.q282FirstRealRuntimeSmokeOnDevicePresent || !input.q281PrivateArtifactLoadSmokePresent || !input.q280NativeRuntimeBridgePresent || !input.q279DependencyTrialPresent || !input.q278EvidenceLockPresent) {
      return reject(GaussOcrResultRealMathLiveReviewRuntimeBindingDecision.blockedMissingRuntimeEvidenceChain, 'missingQ282Q281Q280Q279Q278EvidenceChain');
    }
    if (!input.runtimeCandidateEnvelopeAccepted) {
      return reject(GaussOcrResultRealMathLiveReviewRuntimeBindingDecision.blockedMissingCandidateEnvelope, 'missingRuntimeCandidateEnvelope');
    }
    if (!input.primaryLatexCandidatePreserved) {
      return reject(GaussOcrResultRealMathLiveReviewRuntimeBindingDecision.blockedMissingPrimaryLatexCandidate, 'missingPrimaryLatexCandidate');
    }
    if (!input.alternativeCandidatesPreserved || !input.confidenceWarningsEngineMetadataPreserved) {
      return reject(GaussOcrResultRealMathLiveReviewRuntimeBindingDecision.blockedMissingCandidateMetadata, 'missingCandidateMetadata');
    }
    if (!input.imageInputMetadataPreserved) {
      return reject(GaussOcrResultRealMathLiveReviewRuntimeBindingDecision.blockedMissingImageInputMetadata, 'missingImageInputMetadata');
    }
    if (!input.editableLatexDraftRequired || !input.editableMathLiveReviewRequired || !input.reviewDraftMayBeEditedBeforeImport) {
      return reject(GaussOcrResultRealMathLiveReviewRuntimeBindingDecision.blockedMissingEditableMathLiveReview, 'missingEditableMathLiveReviewBoundary');
    }
    if (!input.userApprovalRequiredBeforeWorkspaceImport || !input.workspaceImportPreparedOnlyAfterUserApproval) {
      return reject(GaussOcrResultRealMathLiveReviewRuntimeBindingDecision.blockedUserApprovalNotRequired, 'missingExplicitUserApprovalBeforeWorkspaceImport');
    }
    if (!input.directWorkspaceImportBlocked) {
      return reject(GaussOcrResultRealMathLiveReviewRuntimeBindingDecision.blockedDirectWorkspaceImportRisk, 'directWorkspaceImportRisk');
    }
    if (!input.directSolveGraphSolutionHistoryBlocked) {
      return reject(GaussOcrResultRealMathLiveReviewRuntimeBindingDecision.blockedDirectSolveGraphSolutionHistoryRisk, 'directSolveGraphSolutionHistoryRisk');
    }
    final unsafeRuntimeClaim = !input.noRealMathLiveReviewRuntimeBindingExecutedInQ284 ||
        !input.noRealImageToLatexInferenceExecutedInQ284 ||
        !input.noRealRuntimeInvocationExecutedInQ284 ||
        !input.noNativeRuntimeBridgeInvokedInQ284 ||
        !input.noPaddleRuntimeAdded ||
        !input.noPaddleOcrDependencyAdded ||
        !input.noNativeHandlerImplementationAdded ||
        !input.noJniBindingAdded ||
        !input.noMethodChannelRuntimeBindingAdded ||
        !input.noRuntimeDependencyAddedToPubspec ||
        !input.noGradleNativeRuntimeDependencyAdded ||
        !input.noModelBinaryBundledInBaseApp ||
        !input.noProductionModelUrlBound ||
        !input.noProductionDownloadEnabled ||
        !input.noRealNetworkDownloadWorkerImplemented ||
        input.reviewBindingPassClaimed ||
        input.ocrPassClaimed ||
        input.releasePassClaimed;
    if (unsafeRuntimeClaim) {
      return reject(GaussOcrResultRealMathLiveReviewRuntimeBindingDecision.blockedUnsafeRuntimeInferenceOrPassClaim, 'unsafeRuntimeInferenceOrPassClaim');
    }

    return GaussOcrResultRealMathLiveReviewRuntimeBindingResult._(
      decision: GaussOcrResultRealMathLiveReviewRuntimeBindingDecision.readyForQ285ApprovedWorkspaceImportRuntimeCourtOnly,
      reviewRuntimeBindingStaticReady: true,
      readyForQ285ApprovedWorkspaceImportRuntimeCourtOnly: true,
      rejectionReasons: const <String>[],
      canOpenEditableMathLiveReviewInQ284: true,
      canImportWorkspaceWithoutUserApproval: false,
      canEvaluate: false,
      canGraph: false,
      canWriteSolutionOrHistory: false,
      canClaimReviewBindingPassInQ284: false,
      canClaimOcrPassInQ284: false,
      bindingLabel: input.bindingLabel,
    );
  }

  static GaussRealMathLiveReviewDraftEnvelope guardedReviewDraftBinding({
    required bool reviewRuntimeBindingFeatureFlagEnabled,
    required String captureId,
    required GaussImageToLatexCandidateEnvelope candidate,
    required Map<String, String> imageInputMetadata,
  }) {
    if (!reviewRuntimeBindingFeatureFlagEnabled) {
      return GaussRealMathLiveReviewDraftEnvelope.blockedFeatureFlagOff(requestId: candidate.requestId, captureId: captureId);
    }
    return GaussRealMathLiveReviewDraftEnvelope.fromQ283Candidate(
      captureId: captureId,
      candidate: candidate,
      imageInputMetadata: imageInputMetadata,
    );
  }
}
